                <p class="font-p"><strong class="preface">Церий</strong> - это химический элемент из группы лантаноидов, серебристый металл.</p>
                <h2>Физические свойства</h2>
                <p class="font-p">Церий представляет собой серебристо-белый вязкий и ковкий металл, легко поддаётся ковке и механической обработке при комнатной температуре.</p>
                <ul>
                    <li>α-форма с кубической кристаллической решёткой типа Cu до температуры 95K</li>
                    <li>β-форма с гексагональной кристаллической решёткой типа La в интервале температур 95—264К</li>
                    <li>γ-форма с кубической кристаллической решёткой типа Cu в интервале температур 263—1035K</li>
                    <li>δ-форма с кубической кристаллической решёткой типа α-Fe при температурах выше 1035K</li>
                </ul>
                <h2>Химические свойства</h2>
                <p class="font-p">Редкоземельный металл, неустойчив на воздухе, постепенно окисляется, превращаясь в белый оксид и карбонат церия. При нагревании до +160…+180 °C на воздухе загорается; порошок церия является пирофорным.</p>
                <p class="font-p">Церий реагирует с кислотами, при кипячении окисляется водой, устойчив к действию щелочей. Энергично взаимодействует с галогенами, халькогенами, азотом и углеродом.</p>
                <h2>Историческая справка</h2>
                <p class="font-p">Назван в честь самой большой из малых планет, Цереры (Ceres), в свою очередь, названной в честь римской богини плодородия.</p>
                <p class="font-p">Немецкий химик М. Г. Клапрот, открывший цериевую землю в 1803 г. почти одновременно со своими шведскими коллегами — В. Хизингером и Й. Я. Берцелиусом, возражал против названия «церий», предлагая «церерий». Берцелиус, однако, отстоял своё название, ссылаясь на трудности произношения того имени, которое предлагал новому элементу Клапрот.</p>
