                <p class="font-p"><strong class="preface">Дубний</strong> - это химический элемент таблицы Менделеева с атомным номером 105,а также до 1997 года в СССР и России был известен как нильсбо́рий (Ns).</p>
                <h2>Историческая справка</h2>
                <p class="font-p">Элемент 105 впервые получен на ускорителе в Дубне в 1970 году группой Г. Н. Флёрова путём бомбардировки ядер ²⁴³Am ионами ²²Ne и независимо в Беркли (США) в реакции ²⁴⁹Cf+¹⁵N→²⁶⁰Db+4n. Рабочая группа ИЮПАК в 1993 году сделала вывод, что честь открытия элемента 105 должна быть разделена между группами из Дубны и Беркли.</p>
                <h2>Происхождение названия</h2>
                <p class="font-p">Советские исследователи предложили назвать новый элемент нильсборием (Ns), в честь Нильса Бора[4], американцы — ганием (Ha), в честь Отто Гана, одного из авторов открытия спонтанного деления урана[2]. Комиссия ИЮПАК в 1994 году предложила название жолиотий (Jl), в честь Жолио-Кюри[5]; до этого элемент официально назывался греческими цифрами  — уннилпентиумом (Unp), то есть просто 105-м. Символы Ns, На, Jl можно было видеть в таблицах элементов, изданных в разные годы. Согласно окончательному решению ИЮПАК в 1997 году этот элемент получил название дубний — в честь российского центра по исследованиям в области ядерной физики, наукограда Дубны.</p>
