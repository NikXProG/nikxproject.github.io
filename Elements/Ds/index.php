                <p class="font-p"><strong class="preface">Дармштадтий</strong> — искусственно синтезированный химический элемент 10-й группы периодической системы (по короткой классификации 8-й группы), атомный номер 110. Для изотопов с массовыми числами 267—273 период полураспада не превышает нескольких миллисекунд. Но для самого тяжёлого из известных изотопов (с массовым числом 281) период полураспада составляет около 10 секунд.</p>
                <h2>Свойства</h2> 
                <p class="font-p">Радиоактивен. Однозначно определить химические характеристики дармштадция тяжело из-за коротких периодов полураспада изотопов дармштадция и ограниченного числа вероятных летучих соединений, которые могут быть изучены в очень небольших масштабах. Дармштадтий должен быть очень тяжелым металлом с плотностью около 34,8 г/см³. Для сравнения, самый плотный известный элемент, для которого измерена его плотность, осмий, имеет плотность всего 22,61 г/см³.</p>               
                <h2>Историческая справка</h2>
                <p class="font-p">Элемент получил название по месту открытия. Впервые синтезирован 9 ноября 1994 в Центре исследований тяжёлых ионов (нем. Gesellschaft für Schwerionenforschung, GSI), Дармштадт, С. Хофманном, В. Ниновым, Ф. П. Хессбергером, П. Армбрустером, Х. Фолгером, Г. Мюнценбергом, Х. Шоттом и другими. Обнаруженный изотоп имел атомную массу 269.</p>
                <p class="font-p">Новый элемент был получен в реакции слияния атомов никеля и свинца в результате бомбардировки свинцовой мишени ионами никеля в ускорителе ионов UNILAC в GSI.</p>
                <p class="font-p">Дармштадтий был четвёртым элементом, обнаруженным в GSI. Между 1981 и 1984 гг. там были получены и выделены элементы 107 (борий), 108 (хассий), 109 (мейтнерий). После открытия дармштадтия там же были синтезированы элементы 111 (рентгений) и 112 (коперниций).</p>
