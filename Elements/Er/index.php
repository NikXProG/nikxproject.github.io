                <p class="font-p"><strong class="preface">Эрбий</strong> - это химический элемент с атомным номером 68, относится к лантаноидам и редкоземельным элементам (иттриевая подгруппа).</p>
                <p class="font-p">Элементарный гольмий — это относительно мягкий и пластичный серебристо-белый металл. Относительно стабилен в сухом воздухе при комнатной температуре и горит в воздухе при нагревании. Растворяется в кислотах[3]. Содержится в таких минералах как монацит и гадолинит и обычно извлекается из монацита с использованием методов ионного обмена. Гольмий обладает самой высокой магнитной проницаемостью среди всех элементов и поэтому используется для полюсов самых сильных статических магнитов. Поскольку гольмий сильно поглощает нейтроны, он также используется в качестве выгорающего поглотителя в ядерных реакторах.</p>
                <h2>Получение</h2>
                <p class="font-p">Металлический эрбий получают электролизом расплава хлорида (фторида) эрбия ErCl3 (ErF3), а также кальцийтермическим восстановлением этих солей.</p>         
                <h2>Историческая справка</h2>
                <p class="font-p">Впервые эрбий был выделен в 1843 году шведским химиком Карлом Густавом Мосандером, из минерала гадолинита, найденного около селения Иттербю. Мосандер обнаружил примеси в концентрате Y2O3 и выделил из него три фракции: иттриевую, розовую terbia (которая содержала современный элемент эрбий) и бесцветную erbia (содержала элемент тербий, нерастворимый оксид тербия имеет коричневый оттенок). Тербий и эрбий, впрочем, некоторое время путали. Тербий был переименован в эрбий после 1860 года, а эрбий в тербий — в 1877 году.</p>
                <p class="font-p">Марк Делафонтен в 1864 году принялся работать с гадолинитом: эрбий и его соединения были детально изучены различными методами, в том числе с применением газовой горелки. Им же были предоставлены довольно ясные доказательства открытия эрбия[3]. Пер Теодор Клеве в 1879 году, изучая эрбий, оставшийся после отделения от иттербия, пришёл к выводу о неоднородности фракции и открыл в его составе ещё два элемента: тулий и гольмий.</p>
                <p class="font-p">Достаточно чистый Er2O3 был независимо выделен в 1905 году Жоржем Урбэном и Чарльзом Джеймсом. Чистый металл был получен лишь в 1934 году Клеммом и Боммером. Только в 1990-х годах китайский оксид эрбия упал в цене достаточно, чтобы использоваться как краситель для стекла.</p>
