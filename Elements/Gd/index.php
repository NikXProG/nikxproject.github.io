                <p class="font-p"><strong class="preface">Гадолиний</strong> - это химический элемент, лантаноид, атомный номер — 64, атомная масса — 157,25.</p>
                <h2>Физические свойства</h2>
                <p class="font-p">Мягкий, вязкий металл серебристо-белого цвета.</p>
                <h3>Изотопы</h3>
                <p class="font-p">Природный гадолиний состоит из шести стабильных изотопов (154Gd, 155Gd, 156Gd, 157Gd, 158Gd и 160Gd) и одного нестабильного 152Gd.</p> 
                <h2>Получение</h2>
                <p class="font-p">Гадолиний получают восстановлением фторида или хлорида гадолиния (GdF3, GdCl3) кальцием. Соединения гадолиния получают разделением оксидов редкоземельных металлов на фракции.</p>             
                <h2>Историческая справка</h2>
                <p class="font-p">Гадолиний открыт в 1880 году Жаном де Мариньяком, который спектроскопически доказал присутствие нового элемента в смеси оксидов редкоземельных элементов. Элемент был назван по имени финского химика Юхана Гадолина.</p>
