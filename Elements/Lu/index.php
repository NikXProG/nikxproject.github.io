                <p class="font-p"><strong class="preface">Лютеций</strong> - это химический элемент, относящийся к группе лантаноидов.</p>
                <h2>Физические свойства</h2>
                <p class="font-p">Лютеций — металл серебристо-белого цвета, легко поддаётся механической обработке. Он является самым тяжёлым элементом среди лантаноидов как по атомному весу, так и по плотности (9,8404 г/см³). Температура плавления лютеция (1663 °C) максимальна среди всех редкоземельных элементов. Благодаря эффекту лантаноидного сжатия среди всех лантаноидов лютеций имеет наименьшие атомный и ионный радиусы.</p>
                <h2>Химические свойства</h2>
                <p class="font-p">При комнатной температуре на воздухе лютеций покрывается плотной оксидной плёнкой, при температуре 400 °C окисляется. При нагреве взаимодействует с галогенами, серой и другими неметаллами.</p>
                <p class="font-p">Лютеций реагирует с неорганическими кислотами с образованием солей. При упаривании водорастворимых солей лютеция (хлоридов, сульфатов, ацетатов, нитратов) образуются кристаллогидраты.</p>
                <p class="font-p">При взаимодействии водных растворов солей лютеция с фтороводородной кислотой образуется очень малорастворимый осадок фторида лютеция LuF3. Это же соединение можно получить при реакции оксида лютеция Lu2O3 с газообразным фтороводородом или фтором.</p>
                <p class="font-p">Гидроксид лютеция образуется при гидролизе его водорастворимых солей.</p>
                <h2>Получение</h2>
                <p class="font-p">Для получения лютеция производится его выделение из минералов вместе с другими тяжёлыми редкоземельными элементами. Отделение лютеция от других лантаноидов ведут методами экстракции, ионного обмена или дробной кристаллизацией, а металлический лютеций получается при восстановлении кальцием из фторида LuF3.</p>
                <h2>Историческая справка</h2>
                <p class="font-p">Элемент в виде оксида в 1907 году независимо друг от друга открыли французский химик Жорж Урбэн, австрийский минералог Карл Ауэр фон Вельсбах и американский химик Чарльз Джеймс. Все они обнаружили лютеций в виде примеси к оксиду иттербия, который, в свою очередь, был открыт в 1878 г. как примесь к оксиду эрбия, выделенному в 1843 г. из оксида иттрия, обнаруженного в 1797 г. в минерале гадолините. Все эти редкоземельные элементы имеют очень близкие химические свойства. Приоритет открытия принадлежит Ж. Урбэну.</p>
