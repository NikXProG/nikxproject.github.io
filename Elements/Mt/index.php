                <p class="font-p"><strong class="preface">Мейтнерий</strong> — химический элемент с атомным номером 109. Принадлежит к 9-й группе периодической таблицы химических элементов (по устаревшей короткой форме периодической системы принадлежит к побочной подгруппе VIII группы, или к группе VIIIB), находится в седьмом периоде таблицы. В природе отсутствует, массовое число наиболее стабильного из известных изотопов равно 278(его атомная масса равна 278,156(5) а. е. м.). Обозначается символом Mt (от лат. Meitnerium). Ранее был известен как унниленний (Une) или эка-иридий. Синтезирован искусственно.</p>
                <h2>Историческая справка</h2>
                <p class="font-p">Впервые получен в 1982 году в Центре исследования тяжёлых ионов (нем. Gesellschaft für Schwerionenforschung, GSI), Дармштадт, Германия в результате реакции <sup>209</sup>Bi + <sup>58</sup>Fe → <sup>266</sup>Mt+n.</p>
                <p class="font-p">Мейтнерий был раньше известен как унниленний (Unnilennium), имея символ Une. Название «мейтнерий» (Мt) было предложено в честь австрийского физика Лизы Мейтнер. Название было официально принято ИЮПАК в 1997 году.</p>
