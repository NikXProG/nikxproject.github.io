                <p class="font-p"><strong class="preface">Неодим</strong> - это химический элемент, редкоземельный металл серебристо-белого цвета с золотистым оттенком. Относится к группе лантаноидов. Легко окисляется на воздухе. Открыт в 1885 году австрийским химиком Карлом Ауэром фон Вельсбахом. Используется как компонент сплавов с алюминием и магнием для самолёто- и ракетостроения[3].</p>
                <h2>Историческая справка</h2>
                <p class="font-p">Неодим был открыт в 1885 году австрийским химиком Карлом Ауэром фон Вельсбахом. Он разделил дидим — смесь двух редкоземельных металлов, трудно разделяемую из-за чрезвычайной близости их химических свойств и ранее считавшуюся индивидуальным химическим элементом, — на неодим и празеодим. Получить неодим в чистом виде, практически свободном от примесей празеодима, удалось лишь в 1925 году.</p>
                <h3>Происхождение названия</h3>
                <p class="font-p">Название «неодим» (лат. Neodymium) представляет собой слияние двух греческих слов νέος «новый» и δίδυμος «близнец», что связано с историей открытия элемента[3].</p>
                <h2>Получение</h2>
                <p class="font-p">Неодим при разделении редкоземельных элементов концентрируется вместе с лёгкими лантаноидами и выделяется вместе с празеодимом; смесь соединений Pr(III) и Nd(III) называют дидимом. Металлический неодим получают из безводных галогенидов электролизом их расплава или кальциетермическим восстановлением. Электролиз расплава NdF3 или NdCl3 (с добавками галогенидов Li, К, Са, Ва) ведут при 1000 °C и катодной плотности тока 4,7 А/см²; материал анода и катода — графит.</p>
