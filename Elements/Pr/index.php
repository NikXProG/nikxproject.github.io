                <p class="font-p"><strong class="preface">Празеодим</strong> - это химический элемент из группы лантаноидов, серебристый металл.</p>
                <h2>Историческая справка</h2>
                <p class="font-p">Празеодим был открыт в 1885 году австрийским химиком Карлом Ауэром фон Вельсбахом. Он установил, что обнаруженный в 1839 году шведским химиком Карлом Мосандером элемент дидим является смесью двух элементов с близкими физическими и химическими свойствами, которым он дал названия неодим и празеодим.</p>
                <h3>Происхождение названия</h3>
                <p class="font-p">От греч. πράσιος — «светло-зелёный» и δίδυμος — «близнец». Таким названием он обязан цвету его солей и истории открытия элемента.</p>
                <h2>Получение</h2>
                <p class="font-p">Празеодим получают в смеси с другими редкоземельными элементами. При экстракционном и хроматографическом разделении и дробной кристаллизации празеодим концентрируется вместе с лёгкими лантаноидами и отделяется вместе с неодимом. Далее празеодим отделяют от неодима и переводят в чистый металл металлотермически или электролизом при 850oC.</p>
