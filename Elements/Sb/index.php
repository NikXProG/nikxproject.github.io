                <p class="font-p"><strong class="preface">Сурьма</strong> - это химический элемент 15-й группы (по устаревшей классификации — главной подгруппы пятой группы) пятого периода периодической системы химических элементов <a href="http://www.scienchemistry.org/web/HistoricalFigures/Mendeleev.html" class="mini-preview-anchor">Д.И.Менделеева<span class="mini-preview-wrapper" style="width: 320px; height: 490px; top: -498px; left: -10px;"><span class="mini-preview-loading" style="height: 478px;"><img class="mini-preview-img" src="http://chemistry.com/HistoricalFigures/Mendeleev/img.jpg"><span class="p"><strong class="word">Д.И.Менделеев</strong> — русский учёный-энциклопедист: химик, физикохимик, физик и так далее. Среди самых известных открытий — периодический закон химических элементов, один из фундаментальных законов мироздания, неотъемлемый для всего естествознания.</span></span></span></a>; имеет атомный номер 51.</p>
                <h2>Физические свойства</h2>
                <p class="font-p">Простое вещество сурьма — полуметалл серебристо-белого цвета с синеватым оттенком, грубозернистого строения. Известны четыре металлических аллотропных модификаций сурьмы, существующих при различных давлениях, и три аморфные модификации (взрывчатая, чёрная и жёлтая сурьма).</p>
                <h2>Химические свойства</h2>
                <p class="font-p">Со многими металлами образует интерметаллические соединения — антимониды. Основные валентные состояния в соединениях: III и V.</p>                
                <p class="font-p">Окисляющие концентрированные кислоты активно взаимодействуют с сурьмой;растворима в царской водке.</p> 
                <ol class="rounded">
                    <li>серная кислота превращает сурьму в сульфат сурьмы(III) с выделением сернистого газа:
                        <br><strong>2Sb + 6H<sub>2</sub>SO<sub>4</sub> → Sb<sub>2</sub> (SO<sub>4</sub>)<sub>3</sub> + 3SO<sub>2</sub>↑ + 6H<sub>2</sub>O</strong>
                    </li>
                    <li>азотная кислота переводит сурьму в сурьмяную кислоту (условная формула H<sub>3</sub> SbO<sub>4</sub>):
                        <br><strong>Sb + 5HNO<sub>3</sub> → H<sub>3</sub> SbO<sub>4</sub> + 5NO<sub>2</sub>↑ + H<sub>2</sub>O</strong>
                    </li>
                    <li>с иодом в инертной атмосфере при незначительном нагревании:
                        <br><strong>2Sb + 3I<sub>2</sub> → 2SbI<sub>3</sub></strong> 
                    </li>
                    <li>с хлором реагирует по-разному, в зависимости от температуры:
                        <br><strong>2Sb + 3Cl<sub>2</sub> → 2SbCl<sub>3</sub></strong> 
                        <br><strong>2Sb + 5Cl<sub>2</sub> → 2BCl<sub>5</sub></strong> 
                    </li> 
                </ol>      
                <h2>Историческая справка</h2>
                <p class="font-p">Сурьма известна с глубокой древности. В странах Востока она использовалась примерно за 3000 лет до н. э. для изготовления сосудов. В Древнем Египте уже в XIX в. до н. э. порошок сурьмяного блеска (природный Sb2S3) под названием mesten или stem применялся для чернения бровей. В Древней Греции он был известен как στίμμι и στίβι, отсюда лат. stibium. Около XII—XIV вв. н. э. появилось название antimonium. Подробное описание свойств и способов получения сурьмы и её соединений впервые дано алхимиком Василием Валентином (Германия) в 1604 году. В 1789 году <a href="http://www.scienchemistry.org/web/HistoricalFigures/Antoine Laurent.html" class="mini-preview-anchor">Антуан Лавуазье<span class="mini-preview-wrapper" style="width: 320px; height: 448px; top: -452px; left: -10px;"><span class="mini-preview-loading" style="height: 436px;"><img class="mini-preview-img" src="http://chemistry.com/HistoricalFigures/Antoine Laurent/img2.jpg"><span class="p"><strong class="word">Антуан Лавуазье</strong> — французский естествоиспытатель, основатель современной химии.</span></span></span></a> включил сурьму в список химических элементов под названием antimoine (современный английский antimony, испанский и итальянский antimonio, немецкий Antimon).</p>
