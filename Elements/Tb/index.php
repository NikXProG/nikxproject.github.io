                <p class="font-p"><strong class="preface">Тербий</strong> - это химический элемент, относящийся к группе лантаноидов.</p>
                <h2>Получение</h2>
                <p class="font-p">Выделяют тербий из смеси редкоземельных элементов методами ионной хроматографии или экстракции.</p>           
                <h2>Историческая справка</h2>
                <p class="font-p">В 1843 году шведский химик К. Г. Мосандер обнаружил примеси в концентрате Y2O3 и выделил из него три фракции: иттриевую, розовую terbia (которая содержала современный элемент эрбий) и бесцветную erbia (содержала элемент тербий, нерастворимый оксид тербия имеет коричневый оттенок). Из-за бесцветности erbia существование этого соединения долгое время подвергалось сомнению, также были перепутаны названия фракций. Тербий в исходном концентрате составлял около 1 %, однако этого было достаточно, чтобы придать ему желтоватый оттенок. Чистый тербий начале XX века первым получил французский химик Жорж Урбэн, использовавший технологию ионного обмена[3].</p>
                <h3>Происхождение названия</h3>
                <p class="font-p">Наряду ещё с тремя химическими элементами (эрбий, иттербий, иттрий) получил название в честь села Иттербю, находящегося на острове Ресарё, входящем в Стокгольмский архипелаг.</p>
