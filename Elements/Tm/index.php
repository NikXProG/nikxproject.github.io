                <p class="font-p"><strong class="preface">Тулий</strong> - это химический элемент. Обозначается символом Tm, атомный номер — 69, относится к группе лантаноидов. Простое вещество тулий представляет собой легко обрабатываемый металл серебристо-белого цвета.</p>
                <h2>Физические свойства</h2>
                <p class="font-p">Элементарный гольмий — это относительно мягкий и пластичный серебристо-белый металл. Относительно стабилен в сухом воздухе при комнатной температуре и горит в воздухе при нагревании. Растворяется в кислотах[3]. Содержится в таких минералах как монацит и гадолинит и обычно извлекается из монацита с использованием методов ионного обмена. Гольмий обладает самой высокой магнитной проницаемостью среди всех элементов и поэтому используется для полюсов самых сильных статических магнитов. Поскольку гольмий сильно поглощает нейтроны, он также используется в качестве выгорающего поглотителя в ядерных реакторах.</p>
                <h2>Химические свойства</h2>
                <p class="font-p">Растворы солей тулия окрашены в зелёный цвет[3].
                    <br><strong>2Tm + 6H20 → 2Tm(OH)3 + 3H2↑</strong>
                <br>Реагирует с кислотами
                    <br><strong>Tm + 6HNO3 → Tm(NO3)3 + 3NO2↑ + 3H2O</strong>
                    <br><strong>2Tm + 6 HCl → 2TmCl3 + 3H2↑</strong></p>         
                <h2>Историческая справка</h2>
                <p class="font-p">Тулий был открыт шведским химиком П. Т. Клеве в 1879 году при поиске примесей к оксиду эрбия Er2O3. Этот же метод ранее позволил К. Г. Мосандеру открыть другие редкоземельные элементы. При выделении примесей Клеве получил два окисла — коричневый оксид гольмия и зелёный оксид тулия. В 1911 году Т. У. Ричардс получил элемент в чистом виде и измерил его атомный вес.</p>
