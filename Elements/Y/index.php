                <p class="font-p"><strong class="preface">Иттрий</strong> - это  химический элемент 3-й группы пятого периода периодической системы химических элементов Д. И. Менделеева, с атомным номером 39. Обозначается символом Y. Простое вещество иттрий — металл светло-серого цвета.</p>
                <h2>Физические свойства</h2>
                <p class="font-p"><strong class="word">Иттрий</strong> — металл светло-серого цвета. Существует в двух кристаллических модификациях:</p>
                    <ol>
                        <li>α-Y с гексагональной решёткой типа магния</li>
                        <li>β-Y с кубической объёмноцентрированной решёткой типа α-Fe</li>
                    </ol>
                <p class="font-p">Иттрий легко поддается механической обработке.</p>
                <h2>Химические свойства</h2>
                <p class="font-p">На воздухе иттрий покрывается плотной защитной оксидной плёнкой. При 370—425 °C образуется плотная чёрная плёнка оксида. Интенсивное окисление начинается при 750 °C. Компактный металл окисляется кислородом воздуха в кипящей воде, реагирует с минеральными кислотами, уксусной кислотой, не реагирует с фтороводородом. Иттрий при нагревании взаимодействует с галогенами, водородом, азотом, серой и фосфором.</p>
                <h2>Историческая справка</h2>
                <p class="font-p">В 1794 году финский химик Юхан Гадолин выделил из минерала иттербита оксид элемента, который он назвал иттрием — по названию шведского населённого пункта Иттербю, находящегося на острове Ресарё, входящем в Стокгольмский архипелаг. В 1843 году Карл Мосандер доказал, что этот оксид на самом деле является смесью оксидов иттрия, эрбия и тербия. Металлический иттрий, содержащий примеси эрбия, тербия и других лантаноидов, был получен впервые в 1828 году Фридрихом Велером.</p>
