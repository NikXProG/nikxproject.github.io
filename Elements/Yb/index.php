                <p class="font-p"><strong class="preface">Иттербий</strong> - это химический элемент, лантаноид, атомный номер — 70. Обозначается символом Yb. Относится к редкоземельным элементам (иттриевая подгруппа). Простое вещество иттербий — металл светло-серого цвета. Существует в двух кристаллических модификациях: α-Yb с кубической решёткой типа меди и β-Yb с кубической объёмноцентрированной решёткой типа α-Fe, температура перехода α↔β 792 °C[2].</p>
                <h2>Химические свойства</h2>
                <p class="font-p">Растворы солей тулия окрашены в зелёный цвет[3].
                    <br><strong>2Yb + 6H2O → 2Yb(OH)3 + 3H2</strong>
                <br>Реагирует с кислотами
                    <br><strong>2Yb + 6HCl → 2YbCl3 + 3H2</strong>
                <br>Реагирует с кислотами
                    <br><strong>4Yb + 3O2 → 2Yb2O3</strong>
                <br>Реагирует с кислотами
                    <br><strong>2Yb + 3S → Yb2S3</strong></p> 
                <h2>Получение</h2>
                <p class="font-p">Основные методы получения иттербия — это восстановление оксида иттербия (III) в вакууме углеродом или лантаном, а также электролизом расплава хлорида YbCl3.</p>         
                <h2>Историческая справка</h2>
                <p class="font-p">Иттербий был открыт Жаном Шарлем Мариньяком в 1878 году в окиси эрбия[3][4].</p>
